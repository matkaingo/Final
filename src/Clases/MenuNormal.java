    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed
        String buscando = aBuscar.getText();
        String nombre = null;
        String category = null;
        Connection conn =null;
        Statement pst=null;
        ResultSet rs=null;
        category=categoria.getSelectedItem().toString();
        
     try{
           Conexion conect1 = new Conexion();
           conn = conect1.getConnection();
           pst = conn.createStatement();
           String sql = null;
           sql  = "select * from informacion ";
           rs=pst.executeQuery(sql);
            
            
           
          
           InfoNormal ob = new InfoNormal();
           while(rs.next()){
               if(category.equals("Canci贸n")){
                   
                   nombre = rs.getString("nombreC");
                }
            if(category.equals("Album")){
                   
                   nombre = rs.getString("Album");
                }
            if(category.equals("Artista")){
                   
                   nombre = rs.getString("Artista");
                }
               
               if(nombre.equals(buscando)){
                   
                   
                   if(category.equals("Canci贸n")){
                       ob.cancion.setText(buscando);
                       ob.artista.setText(rs.getString("Artista"));
                       ob.album.setText(rs.getString("Album"));
                   }
                   if(category.equals("Album")){
                       ob.album.setText(buscando);
                       ob.artista.setText(rs.getString("Artista"));
                       ob.cancion.setText(rs.getString("nombreC"));
                   }
                   if(category.equals("Artista")){
                       ob.artista.setText(buscando);
                       ob.album.setText(rs.getString("Album"));
                       ob.cancion.setText(rs.getString("nombreC"));
                   }
                   JOptionPane.showMessageDialog(null,category+" Encontrad@");
                   ob.setVisible(true);
                   this.setVisible(false);
                   break;
               }
           }
     }
                catch(SQLException e){
            JOptionPane.showMessageDialog(null,"no da");
        }
               
        
    
        
        
    }//GEN-LAST:event_BuscarActionPerformed

